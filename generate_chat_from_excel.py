# Parse data
lines = data_text.strip().split('\n')[1:]  # Skip header
data = []
for line in lines:
    parts = line.split('\t')
    if len(parts) >= 8:
        data.append({
            'tgl_ro': parts[0],
            'tgl_muat': parts[1],
            'pickup': parts[2],
            'tujuan': parts[3],
            'plat': parts[4],
            'type_truck': parts[5],
            'driver': parts[6],
            'kontak': parts[7]
        })

# Group berdasarkan: Tanggal RO + Pickup + Tujuan + Tipe Truck
grouped = defaultdict(list)
for row in data:
    key = (row['tgl_ro'], row['pickup'], row['tujuan'], row['type_truck'])
    grouped[key].append(row)

# Sort by date
sorted_keys = sorted(grouped.keys(), key=lambda x: (
    datetime.strptime(x[0], '%d-%b-%Y') if '-' in x[0] else datetime.strptime(x[0], '%d-%B-%Y'),
    x[1], x[2], x[3]
))

# Generate chat format
chat_output = []

for key in sorted_keys:
    tgl_ro, pickup, tujuan, type_truck = key
    units = grouped[key]
    
    # Format tanggal untuk header
    try:
        dt = datetime.strptime(tgl_ro, '%d-%b-%Y')
    except:
        dt = datetime.strptime(tgl_ro, '%d-%B-%Y')
    tgl_formatted = dt.strftime('%d %B %Y').upper()
    
    # Map WB to TRONTON
    type_display = "TRONTON 50 CBM" if type_truck == "WB" else "CDDL 24 CBM"
    
    # Header request
    chat_output.append(f"REQUEST ORDER ONCALL {tgl_formatted}")
    chat_output.append("")
    chat_output.append(f"{len(units)} UNIT {type_display}")
    chat_output.append(f"Lokasi : {units[0]['pickup']}")
    chat_output.append(f"Rute/tujuan : {units[0]['tujuan']}")
    
    # Units - hanya tulis waktu loading, driver, plat, hp
    for unit in units:
        chat_output.append(f"Waktu loading : {unit['tgl_muat']}")
        chat_output.append(f"Driver : {unit['driver']}")
        chat_output.append(f"Nopol : {unit['plat']}")
        chat_output.append(f"No hp : {unit['kontak']}")
        if len(units) > 1 and unit != units[-1]:  # Jika bukan unit terakhir
            chat_output.append("")

# Write to file
output_path = r"c:\Ngoding\Skripsi\IDP_RAFAY\Skripsi_rafay_IDP\test_bulanan\FEBUARI\MIX_FEB.TXT"
with open(output_path, 'w', encoding='utf-8') as f:
    f.write('\n'.join(chat_output))

print(f"✅ File generated successfully!")
